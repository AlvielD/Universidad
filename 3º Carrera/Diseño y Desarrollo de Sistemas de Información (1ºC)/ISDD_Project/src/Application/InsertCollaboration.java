    /**
     * Performs the action of requesting the data to insert the collaboration,
     * but first, it checks if the expert and the case does not exist. If some
     * of them does not exist then it request to the user the data to create
     * either the one that does not exist or both.
     *
     * @param evt
     */
    private void InsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertButtonActionPerformed

        String CodeCase, CodeExp;
        CodeExp = CodeExpField.getText();
        CodeCase = CodeCaseField.getText();

        boolean expertExists, caseExsists;
        expertExists = em.expertExists(CodeExp);
        caseExsists = pm.existCase(CodeCase);

        // Expert does not exist but case does
        if (!expertExists && caseExsists) {

            JOptionPane.showMessageDialog(this, "The expert does not exist. Please fill the next fields.");

            ExpertNameField.setEnabled(true);
            CountryField.setEnabled(true);
            SexField.setEnabled(true);
            SpecialismField.setEnabled(true);

            CaseNameField.setEnabled(false);
            CaseStartDateField.setEnabled(false);
            CaseEndDateField.setEnabled(false);
        } else if (expertExists && !caseExsists) {   // Case does not exist but expert does

            JOptionPane.showMessageDialog(this, "The case does not exist. Please fill the next fields.");

            ExpertNameField.setEnabled(false);
            CountryField.setEnabled(false);
            SexField.setEnabled(false);
            SpecialismField.setEnabled(false);

            CaseNameField.setEnabled(true);
            CaseStartDateField.setEnabled(true);
            CaseEndDateField.setEnabled(true);
        } else if (!expertExists && !caseExsists) {  // Neither expert nor case exists

            JOptionPane.showMessageDialog(this, "The expert does not exist. Please fill the next fields.");
            JOptionPane.showMessageDialog(this, "The case does not exist. Please fill the next fields.");

            ExpertNameField.setEnabled(true);
            CountryField.setEnabled(true);
            SexField.setEnabled(true);
            SpecialismField.setEnabled(true);

            CaseNameField.setEnabled(true);
            CaseStartDateField.setEnabled(true);
            CaseEndDateField.setEnabled(true);
        }

        if (expertExists && caseExsists) {
            insertCollab(CodeExp, CodeCase);
        }
    }//GEN-LAST:event_InsertButtonActionPerformed

