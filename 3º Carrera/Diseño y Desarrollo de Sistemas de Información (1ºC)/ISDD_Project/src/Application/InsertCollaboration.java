    /**
     * Insert the collaboration on the DB
     */
    private void insertCollab(String CodeExp, String CodeCase) {

        boolean succed = false;
        String StartDate, Desc;

        StartDate = StartDateField.getText();
        Desc = DescriptionField.getText();
        Collaborates col = new Collaborates();

        col.setCodExperto(CodeExp);
        col.setCodcaso(CodeCase);
        col.setFecha(StartDate);
        col.setDescripcionColaboracion(Desc);

        c.startTransaction();
        succed = cm.insertCollaboration(col);

        if (succed) {
            JOptionPane.showMessageDialog(this, "Collaborator inserted succesfully.");
            c.endTransaccionCommit();
        } else {
            c.endfTransaccionRollback();
        }
    }

    /**
     * Insert the exp on the DB
     *
     * @param CodeExp
     */
    private void insertExp(String CodeExp) {

        boolean succed = false;
        String Name, Country, Sex, Specialism;

        Name = ExpertNameField.getText();
        Country = CountryField.getText();
        Sex = SexField.getText();
        Specialism = SpecialismField.getText();
        Expert exp = new Expert(CodeExp, Name, Country, Sex, Specialism);

        c.startTransaction();
        succed = em.insertExpert(exp);

        if (succed) {
            JOptionPane.showMessageDialog(this, "Expert inserted succesfully.");
            c.endTransaccionCommit();
        } else {
            c.endfTransaccionRollback();
        }

    }

    /**
     * Insert the case on the DB
     *
     * @param CodeCase
     */
    private void insertCase(String CodeCase) {

        boolean succed = false;
        String Name, StartDate, EndDate;

        Name = CaseNameField.getText();
        StartDate = CaseStartDateField.getText();
        EndDate = CaseEndDateField.getText();
        Policialcase pcas = new Policialcase(CodeCase, Name, StartDate, EndDate);

        c.startTransaction();
        succed = pm.insertCase(pcas);

        if (succed) {
            JOptionPane.showMessageDialog(this, "Case inserted succesfully.");
            c.endTransaccionCommit();
        } else {
            c.endfTransaccionRollback();
        }

    }

